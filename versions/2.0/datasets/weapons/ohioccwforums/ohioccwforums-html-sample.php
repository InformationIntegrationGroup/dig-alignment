			<div class="content">Buyer asks:  Is this your gun to sell?  Are you an Ohio resident, may I see you Ohio DL or ID?  Optional: Do you have a Ohio CHL?<br /><br />Seller asks: Are you an Ohio resident, may I see you Ohio DL or ID?  How are you paying?  Is there anything that legally bars you from buying or owning a firearm?  Optional: Do you have a Ohio CHL?<br /><br /><br />The above is the minimum.  You can ask or demand further documentation or exchange of info, but I no longer do.<br /><br />My preference to actually conduct the transaction is a secluded spot in a public space (ex: away from other vehicles in a Wally World or shopping plaza parking lot, a large restaurant parking lot, Gander Mountain lot, or gun club).  Or, @ a gun show.  I also keep the firearm in the seller's vehicle away from the casual observer.  When the exchange is made, try to keep the firearm covered or cased for transfer to your vehicle just to avoid unnecessary public observation/ 'man with a gun' call.<br /><br />If the seller is a total stranger, I ask to do a quick inspection or function test.  If you don't know a particular weapons break down or function test, ask the seller.  Most will be happy to show you.</div>

			
