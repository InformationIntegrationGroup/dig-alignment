<b>Title : &nbsp; </b>Sensor Support for the DARPA Autonomous Land Vehicle Program</p>
<br>
<p align="left">
<b>Descriptive Note  : </b>Final rept. Jul 1984-Aug 1987</p>
<br>
<p>
<b>Corporate Author : </b>ENVIRONMENTAL RESEARCH INST OF MICHIGANANN ARBOR</p>
<br>
<p>
<b> Personal Author(s) : </b>Gleichman, K.  ;   Harmon, L.  ;   Miciek, T.  ;   Miller, C.  ;   Zuk, D.</p>
<br>
<p>
<b>Full Text : </b><a href="http://www.dtic.mil/get-tr-doc/pdf?AD=ADA219308">http://www.dtic.mil/get-tr-doc/pdf?AD=ADA219308</a>
</p>
<br>
<p align="left">
<b>Report Date : </b>OCT 1988</p>
<br>
<p align="left">
<b>Pagination or Media Count : </b>79</p>
<br>
<p align="left">
<b>Abstract : </b>Final report on vision-sensor support for the DARPA Autonomous Land Vehicle (ALV) program. ERIM fabricated three laser-ranging sensors (0.82 micron) for the program and one active multispectral ranging (MS) sensor. This report describes the sensors and support provided to the ALV contractors receiving the sensors during the period July 1984 through August 1987. The MS sensor was given only limited laboratory testing and never adapted to the autonomous vehicle. The report also discusses on-going research related to diode-pumped lasers. Keywords: Laser radar; Vision sensors; 3-D sensor; Active sensor; Multispectral sensor; Autonomous land vehicle; ALV.</p>
<br>
<p align="left">
<b>Descriptors : </b>   &nbsp;  *AUTOMATION,   *DETECTORS,   *LASERS,   *VEHICLES,   *RANGE FINDING,   DIODES,   OPTICAL RADAR,   LASER PUMPING,   LABORATORY TESTS,   GROUND VEHICLES,   VISION,   MULTISPECTRAL,   AUTOMATIC TRACKING.</p>
<br>
<p>
<b> Subject Categories  : </b>DEFENSE SYSTEMS<br> &nbsp;  &nbsp;  &nbsp;  OPTICAL DETECTION AND DETECTORS</p>
<br>
<p align="left">
<b>Distribution Statement  : </b>APPROVED FOR PUBLIC RELEASE</p>
