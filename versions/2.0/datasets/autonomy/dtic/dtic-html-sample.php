function getBaseURL()
{
  var hostURL       =  window.location.href;

  var devURL        = "http://web.dtic.mil";
  var prodURL       = "http://www.dtic.mil";

  var devSearchURL  = "http://dev-gsearch.dtic.mil/search";
  var prodSearchURL = "http://dsearch.dtic.mil/search";

  if (hostURL.match(devSearchURL)) // matches DEV
    return devURL;
  else  // default to PROD
    return prodSearchURL;
}

function changeBaseURL(elem)
{
  var absPath = elem.href;
  var relPath = absPath.substring(absPath.indexOf("/",absPath.indexOf(window.location.hostname)),absPath.length);
  elem.href = getBaseURL() + relPath;  
}

function getSearchBaseURL()
{
  var hostURL       =  window.location.href;

  var devURL        = "http://web.dtic.mil";
  var prodURL       = "http://www.dtic.mil";

  var devSearchURL  = "http://dev-gsearch.dtic.mil/search";
  var prodSearchURL = "http://dsearch.dtic.mil/search";

  if (hostURL.match(devURL)) // matches DEV
    return devSearchURL;
  else if (hostURL.match(prodURL)) // matches PROD
    return prodSearchURL;
  else // use dev URL as default
    return devSearchURL;
}

function onSearchSubmit(formElem)
{
  formElem.action = getSearchBaseURL();
  return true;
}

